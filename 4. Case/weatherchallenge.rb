#weather challenge

weather = 'sunny'
case weather
when "raining" then puts "Bring an umbrella unless you want to get wet!"
when "snowing" then puts "Dress Warmly, because if it's snowing it's cold!"
when "sunny" then puts "It's sunny, so go outside and have some fun!"
when "foggy" then puts "Be careful driving!"
when "overcast" then puts "Even though it's dreary, just enjoy the day"
when "cloudy" then puts "Some clouds never hurt anyone"
when "hailing" then puts "Be cautious of those falling ice balls"
else puts "I have no idea what this weather is"
end
